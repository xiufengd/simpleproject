	private static String buildUrl(String host, String path, Map<String, String> querys) throws UnsupportedEncodingException {
    	StringBuilder sbUrl = new StringBuilder();
    	sbUrl.append(host);
    	if (!StringUtils.isBlank(path)) {
    		sbUrl.append(path);
        }
    	if (null != querys) {
    		StringBuilder sbQuery = new StringBuilder();
        	for (Map.Entry<String, String> query : querys.entrySet()) {
        		if (0 < sbQuery.length()) {
        			sbQuery.append("&");
        		}
        		if (StringUtils.isBlank(query.getKey()) && !StringUtils.isBlank(query.getValue())) {
        			sbQuery.append(query.getValue());
                }
        		if (!StringUtils.isBlank(query.getKey())) {
        			sbQuery.append(query.getKey());
        			if (!StringUtils.isBlank(query.getValue())) {
        				sbQuery.append("=");
        				sbQuery.append(URLEncoder.encode(query.getValue(), "utf-8"));
        			}        			
                }
        	}
        	if (0 < sbQuery.length()) {
        		sbUrl.append("?").append(sbQuery);
        	}
        }
    	
    	return sbUrl.toString();
    }
	
	private static HttpClient wrapClient(String host) {
		HttpClient httpClient = HttpClientBuilder.create().build();
		if (host.startsWith("https://")) {
			sslClient(httpClient);
		}
		
		return httpClient;
	}
	
	private static void sslClient(HttpClient httpClient) {
        try {
            SSLContext ctx = SSLContext.getInstance("TLS");
            X509TrustManager tm = new X509TrustManager() {
            	@Override
                public X509Certificate[] getAcceptedIssuers() {
                    return null;
                }
                @Override
                public void checkClientTrusted(X509Certificate[] xcs, String str) {
                	
                }
				@Override
                public void checkServerTrusted(X509Certificate[] xcs, String str) {
                	
                }
            };
            ctx.init(null, new TrustManager[] { tm }, null);
            HttpsURLConnection.setDefaultSSLSocketFactory(ctx.getSocketFactory());
        } catch (KeyManagementException ex) {
            throw new RuntimeException(ex);
        } catch (NoSuchAlgorithmException ex) {
        	throw new RuntimeException(ex);
        }
    }

	/**
	 * 给https请求添加信任
	 * @return
	 * @throws Exception
	 */
	public static CloseableHttpClient buildSSLCloseableHttpClient()
			throws Exception {
		@SuppressWarnings("deprecation")
		SSLContext sslContext = new SSLContextBuilder().loadTrustMaterial(null,
				new TrustStrategy() {
					// 信任所有
					public boolean isTrusted(X509Certificate[] chain,
											 String authType) throws CertificateException {
						return true;
					}
				}).build();
		// ALLOW_ALL_HOSTNAME_VERIFIER:这个主机名验证器基本上是关闭主机名验证的,实现的是一个空操作，并且不会抛出javax.net.ssl.SSLException异常。
		@SuppressWarnings("deprecation")
		SSLConnectionSocketFactory sslsf = new SSLConnectionSocketFactory(
				sslContext, new String[] { "TLSv1" }, null,
				SSLConnectionSocketFactory.ALLOW_ALL_HOSTNAME_VERIFIER);
		return HttpClients.custom().setSSLSocketFactory(sslsf).build();
	}

	public static String post(String url, Map<String, Object> params) throws Exception {
		ArrayList<NameValuePair> pairs = coverParams2NVPS(params);
		return PostHttpRequest(url,pairs);
	}

	private static String PostHttpRequest(String url, List<NameValuePair> params) throws Exception {

		CloseableHttpClient client = HttpClients.createDefault();

		//超时时间
		RequestConfig requestConfig = RequestConfig.custom()
				.setSocketTimeout(300000)
				.setConnectionRequestTimeout(300000)
				.build();
		String result = null;
		try{

			HttpPost request = new HttpPost(url);
			request.setConfig(requestConfig);
			request.setEntity(new UrlEncodedFormEntity(params,"UTF-8"));
			HttpResponse respones = client.execute(request);
			if(respones.getStatusLine().getStatusCode() ==200){
				result = EntityUtils.toString(respones.getEntity(), "UTF-8");
			}

		}catch (Exception e) {
			e.printStackTrace();
		}finally{
			client.close();
		}
		return result;
	}

	private static ArrayList<NameValuePair> coverParams2NVPS(Map<String, Object> params) {
		ArrayList<NameValuePair> pairs = new ArrayList<>();
		if(params == null || params.size() == 0){
			return pairs;
		}
		for (Map.Entry<String, Object> param : params.entrySet()) {
			Object value = param.getValue();
			if(value instanceof String[]){
				String[] values = (String[]) value;
				for (String v : values) {
					pairs.add(new BasicNameValuePair(param.getKey(), v));
				}
			}else{
				if(value instanceof Integer){
					value = Integer.toString((Integer)value);
				} else if (value instanceof Long){
					value = Long.toString((Long) value);
				}
				pairs.add(new BasicNameValuePair(param.getKey(), (String)value));
			}
		}
		return pairs;
	}
